function dragJQ(dragControl, dragContent) {
	var _drag = false, _x, cw, sw, sh;
	 dragContent = typeof dragContent == "undefined" ? dragControl : dragContent;
	$(dragControl).mousedown(function(e) {
		_drag = true;
		_x = e.pageX - parseInt($(dragContent).css("left"));
		cw = 1000;
		sw = parseInt($(dragContent).outerWidth());
		window.getSelection ? window.getSelection().removeAllRanges() : document.selection.empty();
		document.body.setCapture && $(dragContent)[0].setCapture();
		$(dragControl).mousemove(function(e) {
			if (_drag) {
				var x = e.pageX - _x;
				//alert(x)
				x = x < 50 ? x = 50 : x < (cw - sw) ? x : (cw - sw); 
				x = x > 950 ? x = 950 : x < (cw - sw) ? x : (cw - sw);
				$(dragControl).css({ left: (x-($(dragControl).width()/2))});
			}
		}).mouseup(function() {
			 if (_drag) {
				 _x = e.pageX - parseInt($(dragContent).css("left"));
				var x = e.pageX - _x;
				x = x < 110 ? x = 50 : x < 220 ? x = 160 : x < 330 ? x = 270 : x < 440 ? x = 380 : x < 550 ? x = 490 : x < 660 ? x = 600 : x < 770 ? x = 710 : x < 880 ? x = 820 : x < 990 ? x = 930 : x < (cw - sw) ? x : (cw - sw);
