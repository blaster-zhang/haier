                <p>苹果所含的营养既全面又易被人体消化吸收，非常适合婴幼儿食用。</p>
                <p>中型红薯1个、小苹果1个、牛奶适量、酸奶少许</p>
                <p>苹果不仅含有丰富的糖、维生素和矿物质等大脑必需的营养素，而且更重要的是富含锌元素。据研究，锌是人体内许多重要酶的组成部分，是促进生长发育的关键元素。红薯中还含有一种类似雌性激素的物质，对保护宝宝皮肤有一定的作用。</p>
                <p>1、红薯洗净，切厚片，平铺盘中，包上保鲜膜，微波高火加热5分钟左右，可以用筷子扎透即可。 </p>
                <p>2、苹果洗净、切成与红薯等大的块。</p>
                <p>3、熟透的红薯去皮、切大块，与苹果块一并放入海尔搅拌机。</p>
                <p>4、加入适量牛奶，加至食材的1/2处即可，一起搅拌成薯泥。将薯泥过一遍筛，表面浇上少许酸奶。</p>
                <p>1红薯洗净后直接切片，不用去皮，熟透的红薯去皮更加容易。<br>
2红薯片不要太厚，以免不易熟。<br>
3牛奶不要太多，以免失去浓稠的口感。<br>
4薯泥最后过一遍筛，可以滤去红薯中所含的粗纤维，使口感更佳细腻柔滑。<br>
5宝宝多吃苹果有增进记忆、提高智能的效果。</p>
